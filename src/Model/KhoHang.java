package Model;

public class KhoHang {
	private int MaKho;
	private String TenKho;
	private int STT;

	public int getMaKho() {
		return MaKho;
	}

	public void setMaKho(int maKho) {
		MaKho = maKho;
	}

	public String getTenKho() {
		return TenKho;
	}

	public void setTenKho(String tenKho) {
		TenKho = tenKho;
	}

	public int getSTT() {
		return STT;
	}

	public void setSTT(int sTT) {
		STT = sTT;
	}
}
